package version3;

public class Date {
    
    private int month;
    private int day;
    private int year;

    public Date(int month, int day, int year) {
        this.month = month;
        this.day = day;
        this.year = year;
    }

    public Date() {
         this.month = 0;
        this.day = 0;
        this.year = 0;
    }
    
}
